#
# my $expected = calc_american_checksum( $vin );
#	Given a US or Canada vin $vin, in which position 9 is a checksum,
#	recalculate what it should be.  10 => X as a special case.
#
my @checksum_weight = qw(8 7 6 5 4 3 2 10 0 9 8 7 6 5 4 3 2);
fun calc_american_checksum( $vin )
{
	$vin =~ tr/ABCDEFGHJKLMNPRSTUVWXYZ/12345678123457923456789/;
	my $cksum = 0;
	foreach my $pos (0..16)
	{
		my $v = substr($vin,$pos,1);
		my $w = $checksum_weight[$pos];
		$cksum += $v * $w;
	}
	$cksum %= 11;
	$cksum = 'X' if $cksum == 10;
	return $cksum;
}



#
# my( $country, $errmsg ) = validate_vin( $vin );
#	Validate a given VIN $vin,
#	returning ( country, undef ) if it's ok, and
#	( country, an error message ) if it's not.
#
fun validate_vin( $vin )
{
	my $l = length($vin);
	return ( undef, "vin $vin length $l not 17" ) unless $l==17;
	return ( undef, "vin $vin contains illegal char 'I'" ) if $vin =~ /I/;
	return ( undef, "vin $vin contains illegal char 'O'" ) if $vin =~ /O/;
	return ( undef, "vin $vin contains illegal char 'Q'" ) if $vin =~ /Q/;
	my $countrycode = substr( $vin, 0, 2 );
	my $country = $country{$countrycode};

	return ( undef, "unknown country code $countrycode" )
		unless defined $country;
	return ( undef, "unassigned country code $countrycode" )
		if $country eq "not assigned";
	#print "debug: country=$country\n";

	my $wmicode = substr( $vin, 0, 3 );
	my $wmi = $wmi{$wmicode};
	return ( $country, "unknown wmi code $wmicode" ) unless defined $wmi;
	my $yearcode = substr( $vin, 9, 1 );
	return ( $country, "yearcode $yearcode is illegal char 'U'" )
		if $yearcode =~ /U/;
	return ( $country, "yearcode $yearcode is illegal char 'Z'" )
		if $yearcode =~ /Z/;
	return ( $country, "yearcode $yearcode is illegal char '0'" )
		if $yearcode =~ /0/;

	# US and Canada has a checksum - validate it
	if( $country eq "United States" || $country eq "Canada" )
	{
		my $checksum = substr( $vin, 8, 1 );
		my $expected = calc_american_checksum( $vin );
		return ( $country, "checksum $checksum in $vin, should be $expected" )
			unless $checksum eq $expected;
	}
	return ( $country, undef );
}


