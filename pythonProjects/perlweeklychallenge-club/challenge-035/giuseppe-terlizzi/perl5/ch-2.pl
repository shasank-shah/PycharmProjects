#!/usr/bin/perl

# https://perlweeklychallenge.org/blog/perl-weekly-challenge-035/

# dot: 1
# dash: 111
# intra-character gap: 0
# character gap: 000
# word gap: 0000000

use strict;
use warnings;
use v5.10;


my %morse = (
    '10111'          => 'a',
    '111010101'      => 'b',
    '11101011101'    => 'c',
    '1110101'        => 'd',
    '1'              => 'e',
    '101011101'      => 'f',
    '111011101'      => 'g',
    '1010101'        => 'h',
    '101'            => 'i',
    '1011101110111'  => 'j',
    '111010111'      => 'k',
    '101110101'      => 'l',
    '1110111'        => 'm',
    '11101'          => 'n',
    '11101110111'    => 'o',
    '10111011101'    => 'p',
    '1110111010111'  => 'q',
    '1011101'        => 'r',
    '101010'         => 's',
    '111'            => 't',
    '1010111'        => 'u',
    '101010111'      => 'v',
    '101110111'      => 'w',
    '11101010111'    => 'x',
    '1110101110111'  => 'y',
    '11101110101'    => 'z',

    '1110111011101110111' => 0,
    '10111011101110111'   => 1,
    '101011101110111'     => 2,
    '1010101110111'       => 3,
    '10101010111'         => 4,
    '101010101'           => 5,
    '11101010101'         => 6,
    '1110111010101'       => 7,
    '111011101110101'     => 8,
    '11101110111011101'   => 9,

    '0000000' => ' ',
);

my $string = shift;
$string =~ s/0000000/ /;

my @items = split /000/, $string;
my $words = '';
foreach ( @items ) {

    my $b = $morse{$_} || ' ';
    $words .= $b;

}

say $words;
