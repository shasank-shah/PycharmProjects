# Operators for string

# The '+' Operator acts as a concatenation
str = 'Java is a ' + 'horrible language'
print(str)

fnam = input("May I have your first name, please?")
lnam = input("May I have your last namem, please?")
print("Thank you.")
print("Your name is " + lnam + ". " + fnam + " " + lnam + ".")

# The '*' Operator acts as a concatenation
str = 'Java is a horrible language' * 3
print(str)

str = 3 * "an"
print(str)

str = "string" * -1
print(str)

str = 5 * "2"
print(str)
