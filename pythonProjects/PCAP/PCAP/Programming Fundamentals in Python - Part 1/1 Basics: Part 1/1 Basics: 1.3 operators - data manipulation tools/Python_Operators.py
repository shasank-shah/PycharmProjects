#   Add
print(2 + 2)

#   Exponent
print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)

#   Multiplication
print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.)
print(2 ** 2 ** 3)

#   Divisional with float values
print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)

#   Divisional without float values
print(7 // 4)
print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)

print(-6 // 4)
print(6. // -4)

#   Remainder
print(14 % 4)

# Operators and their binding
print(9 % 6 % 3)

#   Paranthesses
print(5 * ((25 % 13) + 100) / (2 * 13) // 2)
